package Seccion03_Variables;

public class EJ03_Reglas_nombre_variables 
{
  public static void main(String[] args) 
  {
    // Reglas de nombres de variables.
    String nombreCompleto = "Juan Carlos";
    System.out.println("Nombre Completo: " + nombreCompleto);
    String _apellido = "López";
    System.out.println("Apellido: " + _apellido );
    int totalPiezas = 10;
    System.out.println("Total de piezas: " + totalPiezas);
    boolean esCasado = true;
    System.out.println("¿Esta casado?: " + esCasado);
    boolean tieneSaldo = false;
    System.out.println("¿Tiene saldo?: " + tieneSaldo);
  }
}
